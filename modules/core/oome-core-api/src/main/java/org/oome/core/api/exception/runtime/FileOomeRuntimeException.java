package org.oome.core.api.exception.runtime;

public class FileOomeRuntimeException extends BaseOomeRuntimeException {
    public FileOomeRuntimeException() {
        super();
    }

    public FileOomeRuntimeException(String message) {
        super(message);
    }

    public FileOomeRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public FileOomeRuntimeException(Throwable cause) {
        super(cause);
    }
}
